# Вычислить число ПИ c заданной точностью *d*
# Пример:
# при d = 0.001, π = 3.141
# при d = 0.1, π = 3.1
# при d = 0.00001, π = 3.14154
# d от 0.1 до 0.0000000001

import math
d = float(input('Задайте точность числа, d = '))
a = d
count = 0
while d % 10 > 0:
    d = d * 10
    count = count + 1
b = round(math.pi, count - 1)
print(f'Число π = {b} с точностью d = {a}')